package baitap.tuhoc;

public class ToanTuToanHoc {
    public static void main(String[] args){
        double so1 = 12.5, so2 = 3.5, ketQua;
        ketQua = so1 + so2;
        System.out.println("so1 + so2 = " + ketQua);

        ketQua = so1 - so2;
        System.out.println("so1 - so2 = " + ketQua);

        ketQua = so1 * so2;
        System.out.println("so1 * so2 = " + ketQua);

        ketQua = so1 / so2;
        System.out.println("so1 / so2 = " + ketQua);

        ketQua = so1 % so2; //Lấy phần dư của so1 và so2
        System.out.println("so1 % so2 = " + ketQua);
        
    }
    
}
