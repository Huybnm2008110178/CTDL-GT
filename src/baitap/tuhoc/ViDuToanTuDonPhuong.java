package baitap.tuhoc;

public class ViDuToanTuDonPhuong {
    public static void main(String[] args){
        double so = 5.2, ketQua;
        boolean giaTriSai = false;

        System.out.println("Kết quả: +so = " + +so);
        //kết quả: +so = 5.2

        System.out.println("Kết quả: -so = " + -so);
        //kết quả: -so = -5.2

        System.out.println("Kết quả: so++ = " + ++so);
        //kết quả: so++ = 6.2

        System.out.println("Kết quả: so-- = " + --so);
        //kết quả: so-- = 5.2

        System.out.println("Kết quả: !giaTriSai = " + !giaTriSai);
        // !giaTriSai = true 
    }
    
}
